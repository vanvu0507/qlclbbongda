    private void txtLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtLuuActionPerformed
       StringBuilder sb = new StringBuilder();
       DataValidator.validateEmpty(txtMacauthu, sb,"Mã cầu thủ không được để trống");
       DataValidator.validateEmpty(txtTen, sb, "Tên cầu thủ không được để trống");
       if(sb.length()>0){
           MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
           return;
       }
        try {
           Cauthu ct = new Cauthu();
           ct.setMacauthu(txtMacauthu.getText());
           ct.setTen(txtTen.getText());
           ct.setViTri(txtVitri.getText());
           ct.setSoAo(txtSoao.getText());
           ct.setChieuCao(txtChieucao.getText());
           ct.setCanNang(txtCannang.getText());
           ct.setNamSinh(txtNamsinh.getText());
           ct.setHinh(personalImage);
           
           CauThuDao dao = new CauThuDao();
           if(dao.findById(txtMacauthu.getText()) != null){
               if(MessageDialogHelper.showConfirmDialog(parentForm, 
                       "Bạn có muốn cập nhật Cầu thủ không ?","Hỏi")==JOptionPane.NO_OPTION){
                   return;
               }
               if(dao.update(ct)){
                   MessageDialogHelper.showMessageDialog(parentForm,"Cầu thủ đã được cập nhật","Thông báo");
               }else{
                   MessageDialogHelper.showMessageDialog(parentForm,"Không thể cập nhật Cầu thủ","Thông báo");
               }
           }else{
           if(dao.insert(ct)){
               MessageDialogHelper.showMessageDialog(parentForm, "Cầu thủ đã được lưu","Thông báo");
               loadDataToTable();
           }else{
               MessageDialogHelper.showConfirmDialog(parentForm, "Cầu thủ không được lưu do lỗi","Cảnh báo");
           }
           }
       } catch (Exception e) {
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,
                   e.getMessage(),"Lỗi");
       }
    }//GEN-LAST:event_txtLuuActionPerformed

