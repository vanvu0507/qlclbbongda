    private void tblPlayersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPlayersMouseClicked
       try {
           int row = tblPlayers.getSelectedRow();
           
           if(row >= 0){
               String ma = (String) tblPlayers.getValueAt(row, 0);
               CauThuDao dao  = new CauThuDao();
               Cauthu ct =dao.findById(ma);
           
               if(ct != null){
                   txtMacauthu.setText(ct.getMacauthu());
                   txtTen.setText(ct.getTen());
                   txtVitri.setText(ct.getViTri());
                   txtSoao.setText(ct.getSoAo());
                   txtChieucao.setText(ct.getChieuCao());
                   txtCannang.setText(ct.getCanNang());
                   txtNamsinh.setText(ct.getNamSinh());
                   if(ct.getHinh() != null){
                       Image img = ImageHelper.createImageFromByteArray(ct.getHinh(),"jpg");
                       lblImage.setIcon(new ImageIcon(img));
                       personalImage = ct.getHinh();
                   }else{
                         personalImage = ct.getHinh();
                         ImageIcon icon = new ImageIcon(
            getClass().getResource("/icon/athlete (3).png"));
    lblImage.setIcon(icon);
                   }
               }
          }
       }catch (Exception e){
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
       }
    }//GEN-LAST:event_tblPlayersMouseClicked

