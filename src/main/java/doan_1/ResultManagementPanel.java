    private void loadDataToTable(){
        try {
            KetQuaThiDauDao dao = new KetQuaThiDauDao();
            List <Ketquathidau> list = dao.findAllKetQuaThiDau();
            tblModel.setRowCount(0);
            for(Ketquathidau kq : list){
                tblModel.addRow(new Object[]{
                   kq.getMaTran(),kq.getTranDau(),kq.getNgay(),kq.getGio(),kq.getTiSo()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
        }
    }

