    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
    try {
        if(txtMatran.getText().equals("")){
            MessageDialogHelper.showMessageDialog(parentForm,"Mã trận cần phải nhập" ,"Lỗi");
            return;
        }
        
        KetQuaThiDauDao dao = new KetQuaThiDauDao();
        Ketquathidau kq = dao.findByMaTran(txtMatran.getText());
        if(kq == null){
            MessageDialogHelper.showMessageDialog(parentForm,"Mã trận không tồn tại trong bảng","Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xoá trận đấu không ?","Hỏi")
                == JOptionPane.NO_OPTION){
            return;
        }
        if(dao.deleteByMaTran(txtMatran.getText())){
            MessageDialogHelper.showMessageDialog(parentForm,"Trận đấu đã được xoá","Thông báo");
        }else{
            MessageDialogHelper.showMessageDialog(parentForm,"Trận đấu chưa thể xoá","Thông báo");
        }
        loadDataToTable();
    } catch (Exception e) {
        e.printStackTrace();
        MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
    }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblResultsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultsMouseClicked
       try {
           int row = tblResults.getSelectedRow();
           
           if(row >= 0){
               String ma = (String) tblResults.getValueAt(row, 0);
               KetQuaThiDauDao dao  = new KetQuaThiDauDao();
               Ketquathidau lt =dao.findByMaTran(ma);
           
               if(lt != null){
                   txtMatran.setText(lt.getMaTran());
                   txtTran.setText(lt.getTranDau());
                   txtNgay.setText(lt.getNgay());
                   txtGio.setText(lt.getGio());
                   txtTiso.setText(lt.getTiSo());
                   
               }
          }
       }catch (Exception e){
           e.printStackTrace();
           MessageDialogHelper.showErrorDialog(parentForm,e.getMessage(),"Lỗi");
       }
    }//GEN-LAST:event_tblResultsMouseClicked

